//Function
void DisplayASCII( )
{
	int iCnt = 0;

		printf("--------------------------------------------------------------------\n");
		printf("|  Symbol   |   \tDecimal\t   | HexaDecimal   |   Octal\t   |\n");
		printf("--------------------------------------------------------------------\n");

	for( iCnt=0; iCnt<=255; iCnt++)
		printf("|   %c    |   \t%d\t   |   \t%X\t   |   \t%o\t   |\n",iCnt,iCnt,iCnt,iCnt);

		printf("--------------------------------------------------------------------\n");
}


